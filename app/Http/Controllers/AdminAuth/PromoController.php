     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $promo = Promo::find($id);
        $promo->promo = $request->input('promo');
        $promo->product_id = $request->input('product_id');

        $promo->save();

        return redirect('admin/promo');

